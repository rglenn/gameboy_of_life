void main()
{
    setup();
    while (1)
    {
        loop();
    }
}

void setup()
{
    DISPLAY_ON;
    set_bkg_data(0, 2, backgrounds);
    SHOW_BKG;
}

BOOLEAN toggle = TRUE;
UWORD y;
UWORD x;
UWORD index;
void loop()
{
    y = 0;
    x = 0;
    while(1) {

        index = x + y * WIDTH;
        background_map[index] = toggle;
        show_board();

        if(x == WIDTH - 1) {
            if(y == HEIGHT - 1) {
                break;
            } else {
                x = 0;
                y++;
            }
        } else {
            x++;
        }
    }
    toggle = !toggle;
}

void show_board()
{
    wait_vbl_done();
    set_bkg_tiles(0, 0, WIDTH, HEIGHT, background_map);
}