void setup() {
    DISPLAY_ON;
    set_bkg_data(0, 2, backgrounds);
}

BOOLEAN toggle = TRUE;
void loop() {
    UWORD y = 0;
    UWORD x = 0;
    for (y = 0; y < HEIGHT; y++)
    {
        for (x = 0; x < WIDTH; x++)
        {
            gameboard[y * WIDTH + x] = toggle;
        }
    }
    toggle = !toggle;
    x++;
}